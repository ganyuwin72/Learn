public class VariableDemo3{
	public static void main(String[] args){
		//数据类型
		//1.byte类型
		byte b = 10;
		System.out.println(b);
		//2.short类型
		short s = 20;
		System.out.println(s);
		//3.int类型
		int i = 30;
		System.out.println(i);
		//4.long类型
		//long类型要在数字值后面加L（l）作为后缀
		long l = 9999999999L;
		System.out.println(l);
		//5.float类型
		//float类型要在数字值后面加F（f）作为后缀
		float f = 19.2F;
		System.out.println(f);
		//6.double类型
		double d = 23.45;
		System.out.println(d);
		//7.char类型
		char c = '中';
		System.out.println(c);
		//8.boolean类型
		//true false
		boolean o = true;
		System.out.println(o);
	}
}