public class ArithmeticoperatorDemo1 {
    public static void main(String[] args) {
        //+
        System.out.println(10+20);
        //-
        System.out.println(10-20);
        //*
        System.out.println(10*20);

        //小数运算
        //代码中，如果有小数参与运算，结果可能是不精确的
        //+
        System.out.println(10.0 + 20.3);
        //-
        System.out.println(10.9 - 18.93);
        //*
        System.out.println(10.3 * 0.36);
    }
}
