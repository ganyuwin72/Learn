public class CompareoperatorDemo1 {
    public static void main(String[] args) {
        //关系运算符
        //==,!=,>=,<=,>,<

        //==
        int a = 10;
        int b = 10;
        int c = 20;
        System.out.println(a == b);
        System.out.println(a == c);

        //!=
        System.out.println(a != b);
        System.out.println(a != c);
    }
}
